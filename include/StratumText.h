static const char* TEXT_VSH = R"(
attribute vec2 pos;
attribute vec2 uv;
varying vec2 vUV;
void main() { vUV = uv; gl_Position = vec4(pos, 0.0, 1.0); }
)";

static const char* TEXT_FSH = R"(
precision mediump float;
varying vec2 vUV;
uniform sampler2D tex;
uniform vec4 color;
void main() {
    float a = texture2D(tex, vUV).r;
    gl_FragColor = vec4(color.rgb, color.a * a);
}
)";

class TextRenderer {
public:
    GLuint prog = 0, tex = 0, vbo = 0;
    GLint  posLoc, uvLoc, texLoc, colorLoc;
    float  aspect = 1.0f;

    void init(float asp) {
        aspect = asp;

        uint8_t atlas[8][1024];
        memset(atlas, 0, sizeof(atlas));
        for (int c = 0; c < 128; c++) {
            for (int row = 0; row < 8; row++) {
                uint8_t bits = FONT8x8[c][row];
                for (int col = 0; col < 8; col++)
                    atlas[row][c * 8 + col] = (bits & (0x80 >> col)) ? 255 : 0;
            }
        }

        glGenTextures(1, &tex);
        glBindTexture(GL_TEXTURE_2D, tex);
        glTexImage2D(GL_TEXTURE_2D, 0, GL_LUMINANCE, 1024, 8, 0,
                     GL_LUMINANCE, GL_UNSIGNED_BYTE, atlas);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
        glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);

        auto compile = [](GLenum type, const char* src) {
            GLuint s = glCreateShader(type);
            glShaderSource(s, 1, &src, nullptr);
            glCompileShader(s);
            return s;
        };
        GLuint vs = compile(GL_VERTEX_SHADER,   TEXT_VSH);
        GLuint fs = compile(GL_FRAGMENT_SHADER, TEXT_FSH);
        prog = glCreateProgram();
        glAttachShader(prog, vs); glAttachShader(prog, fs);
        glLinkProgram(prog);

        posLoc   = glGetAttribLocation(prog,  "pos");
        uvLoc    = glGetAttribLocation(prog,  "uv");
        texLoc   = glGetUniformLocation(prog, "tex");
        colorLoc = glGetUniformLocation(prog, "color");

        glGenBuffers(1, &vbo);
    }

    void draw(const char* str, float x, float y, float size,
              float r, float g, float b, float a = 1.0f) {
        if (!prog || !str) return;
        int len = strlen(str);
        _drawChars(str, len, x, y, size, r, g, b, a);
    }

    // wraps at word boundaries, lineH = line height multiplier (default 1.5)
    void drawWrapped(const char* str, float x, float y, float size, float maxW,
                     float r, float g, float b, float a = 1.0f, float lineH = 1.5f) {
        if (!prog || !str) return;
        float charW = size / aspect;
        float curY  = y;

        char  word[256];
        float lineX = x;

        const char* p = str;
        while (*p) {
            // collect word
            int wlen = 0;
            while (*p && *p != ' ' && *p != '\n') word[wlen++] = *p++;
            word[wlen] = 0;

            float wordW = wlen * charW;

            if (*p == '\n') {
                // explicit newline — flush word then break
                if (wlen > 0) {
                    _drawChars(word, wlen, lineX, curY, size, r, g, b, a);
                    lineX += wordW;
                }
                curY  += size * lineH;
                lineX  = x;
                p++;
                continue;
            }

            // would this word overflow?
            if (lineX + wordW > x + maxW && lineX > x) {
                curY  += size * lineH;
                lineX  = x;
            }

            if (wlen > 0) {
                _drawChars(word, wlen, lineX, curY, size, r, g, b, a);
                lineX += wordW;
            }

            // space
            if (*p == ' ') {
                lineX += charW;
                p++;
            }
        }
    }

private:
    void _drawChars(const char* str, int len, float x, float y, float size,
                    float r, float g, float b, float a) {
        float charW = size / aspect;
        float charH = size;

        float* verts = new float[len * 6 * 4];
        int vi = 0;

        for (int i = 0; i < len; i++) {
            unsigned char c = (unsigned char)str[i];
            if (c >= 128) c = '?';

            float x0 = (x + i * charW) * 2.0f - 1.0f;
            float x1 = (x + i * charW + charW) * 2.0f - 1.0f;
            float y0 = 1.0f - y * 2.0f;
            float y1 = 1.0f - (y + charH) * 2.0f;

            float u0 = (c * 8.0f) / 1024.0f;
            float u1 = (c * 8.0f + 8.0f) / 1024.0f;

            verts[vi++]=x0; verts[vi++]=y0; verts[vi++]=u0; verts[vi++]=0.0f;
            verts[vi++]=x1; verts[vi++]=y0; verts[vi++]=u1; verts[vi++]=0.0f;
            verts[vi++]=x0; verts[vi++]=y1; verts[vi++]=u0; verts[vi++]=1.0f;
            verts[vi++]=x1; verts[vi++]=y0; verts[vi++]=u1; verts[vi++]=0.0f;
            verts[vi++]=x1; verts[vi++]=y1; verts[vi++]=u1; verts[vi++]=1.0f;
            verts[vi++]=x0; verts[vi++]=y1; verts[vi++]=u0; verts[vi++]=1.0f;
        }

        GLint prevProg; glGetIntegerv(GL_CURRENT_PROGRAM, &prevProg);
        glUseProgram(prog);
        glEnable(GL_BLEND);
        glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

        glBindBuffer(GL_ARRAY_BUFFER, vbo);
        glBufferData(GL_ARRAY_BUFFER, vi * sizeof(float), verts, GL_STREAM_DRAW);

        glEnableVertexAttribArray(posLoc);
        glEnableVertexAttribArray(uvLoc);
        glVertexAttribPointer(posLoc, 2, GL_FLOAT, GL_FALSE, 4*sizeof(float), (void*)0);
        glVertexAttribPointer(uvLoc,  2, GL_FLOAT, GL_FALSE, 4*sizeof(float), (void*)(2*sizeof(float)));

        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, tex);
        glUniform1i(texLoc, 0);
        glUniform4f(colorLoc, r, g, b, a);

        glDrawArrays(GL_TRIANGLES, 0, len * 6);

        glDisableVertexAttribArray(posLoc);
        glDisableVertexAttribArray(uvLoc);
        glBindBuffer(GL_ARRAY_BUFFER, 0);
        glUseProgram(prevProg);
        delete[] verts;
    }
};

// global singleton — call Text::init(aspect) once after s.init()
namespace Text {
    static TextRenderer _r;
    static bool         _ready = false;

    inline void init(float aspect) {
        if (!_ready) { _r.init(aspect); _ready = true; }
    }

    inline void draw(const char* str, float x, float y, float size,
                     float r, float g, float b, float a = 1.0f) {
        _r.draw(str, x, y, size, r, g, b, a);
    }

    inline void drawWrapped(const char* str, float x, float y, float size, float maxW,
                            float r, float g, float b, float a = 1.0f, float lineH = 1.5f) {
        _r.drawWrapped(str, x, y, size, maxW, r, g, b, a);
    }

    // convenience: char width in UV space at given size
    inline float charW(float size, float aspect) { return size / aspect; }
}
